    private static int getRandomInt(int min, int max) {
        return ThreadLocalRandom.current().nextInt(min, max);
    }

    private static long getRandomLong(long min, long max) {
        return ThreadLocalRandom.current().nextLong(min, max);
    }

    private static String rndStrFromArr(String[] arr) {
        int index = getRandomInt(0, arr.length);
        return arr[index];
    }

    public static Employee GenerateRandomMaleStartingWithF() {
        Employee.Gender gender = Employee.Gender.MALE;
        String firstName = rndStrFromArr(MALE_FIRST_NAME);
        String middleName = rndStrFromArr(MALE_MIDDLE_NAME);
        String lastName = rndStrFromArr(F_MALE_LASTNAME);
        LocalDate maxBirthday = LocalDate.now().minusYears(18);
        LocalDate minBirthday = LocalDate.now().minusYears(63);
        LocalDate birthday = LocalDate.ofEpochDay(getRandomLong(minBirthday.toEpochDay(), maxBirthday.toEpochDay()));
        return new Employee(String.format("%s %s %s", lastName, firstName, middleName), birthday, gender);
    }

    public static Employee GenerateRandomEmployee() {
        Employee.Gender gender = getRandomInt(0, 2) == 0 ? Employee.Gender.MALE : Employee.Gender.FEMALE;
        String firstName;
        String middleName;
        String lastName;
        LocalDate minBirthday;
        LocalDate maxBirthday = LocalDate.now().minusYears(18);

        if (gender == Employee.Gender.MALE) {
            firstName = rndStrFromArr(MALE_FIRST_NAME);
            middleName = rndStrFromArr(MALE_MIDDLE_NAME);
            lastName = rndStrFromArr(MALE_LAST_NAME);
            minBirthday = LocalDate.now().minusYears(63);
        } else {
            firstName = rndStrFromArr(FEMALE_FIRST_NAME);
            middleName = rndStrFromArr(FEMALE_MIDDLE_NAME);
            lastName = rndStrFromArr(FEMALE_LAST_NAME);
            minBirthday = LocalDate.now().minusYears(60);
        }


        return new Employee(String.format("%s %s %s", lastName, firstName, middleName),
                LocalDate.ofEpochDay(getRandomLong(minBirthday.toEpochDay(), maxBirthday.toEpochDay())),
                gender);
    }


